# Для заданого цілого числа n вивести відповідь YES,
# якщо виконується хоча б одна з наступних умов і NO у протилежному випадку.
# число n непарне;
# число n додатне і трицифрове.

n = int(input())

check_1 = n % 2 == 1   # число n непарне;
check_2 = 100 <= n <= 999

check = check_1 or check_2

if check == True:
    print("YES")
else:
    print("NO")
