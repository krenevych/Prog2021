from base import *

a = readMatrix(2, float)
printMatrix(a, "%6.1f")

# a[0][0]  a[0][1]  a[0][2]
# a[1][0]  a[1][1]  a[1][2]
#
# det =
#   | a[0][0]  a[0][1] |
#   | a[1][0]  a[1][1] | =
#  = a[0][0]*a[1][1] - a[0][1]*a[1][0]
#
# detX =
#   | a[0][2]  a[0][1] |
#   | a[1][2]  a[1][1] | =
# = a[0][2]*a[1][1] - a[0][1]*a[1][2]
#
# detY =
#   | a[0][0]  a[0][2] |
#   | a[1][0]  a[1][2] | =
# = a[0][0]*a[1][2] - a[0][2]*a[1][0]

det = a[0][0] * a[1][1] - a[0][1] * a[1][0]
detX = a[0][2] * a[1][1] - a[0][1] * a[1][2]
detY = a[0][0] * a[1][2] - a[0][2] * a[1][0]

x = detX / det
y = detY / det

print("%0.3f" % x)
print("%0.3f" % y)
