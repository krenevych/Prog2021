# Задано рядок, серед елементів якого містяться цифри.
# Використовуючи для всіх символів рядка функцію int
# перетворення символа у число, обчислити суму цифр заданого рядка.

s = "234k44l145lk'12341436456lkj1;l235"
# s = "12345"

suma = 0
for c in s:
    try:
        suma += int(c)
    except ValueError:
        pass

print(suma)
