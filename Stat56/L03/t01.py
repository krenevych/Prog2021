# Написати програму для обчислення добутку
# двох натуральних чисел, використовуючи
# лише операцію додавання.

# 5 * 4 = 4 + 4 + 4 + 4 + 4
# n * m = m + ... + m

n, m = map(int, input().split())

i = 0
mult_nm = 0
while i < n:
    mult_nm = mult_nm + m
    i = i + 1

print("%d * %d = %d" % (n , m, mult_nm))